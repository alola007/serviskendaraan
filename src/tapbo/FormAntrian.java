    private void insertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertActionPerformed
if (Validasi()) {
            antriDao = new AntrianDAO();
            Antrian antrian = new Antrian();
            antrian.setNama(fieldPel.getText());
            antrian.setNoPol(fieldNoPol.getText());
            antrian.setNoStnk(fieldNoStnk.getText());

            if (antrian != null) {
                try {
                    antriDao.insert(antrian);
                    daftar();
                    JOptionPane.showMessageDialog(this, "Data telah berhasil disimpan.", "Simpan Data", JOptionPane.INFORMATION_MESSAGE);
                    empty();
                } catch (SQLException ex) {
//                    ex.printStackTrace();
                    JOptionPane.showMessageDialog(this, "Antrian Sudah Ada");
                    empty();
                }
            }
        }
    }//GEN-LAST:event_insertActionPerformed

