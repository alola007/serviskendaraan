    private boolean Validasi() {
        boolean Valid = true;
        String Pesan = "Silahkan Cek Kembali Pengisian Data";

        if (fieldPel.getText().equals("")) {
            Pesan += "\nSilahkan Isi Nama Pelanggan";
            Valid = false;
        }
        if (fieldNoPol.getText().equals("")) {
            Pesan += "\nSilahkan Isi No Polisi Kendaraan";
            Valid = false;
        }
        if (fieldNoStnk.getText().equals("")) {
            Pesan += "\nSilahkan Isi Nomor STNK";
            Valid = false;
        }
        
        return Valid;
    }
    
    public void daftar() {
        String kolom[] = {"Nama Pelanggan", "No Polisi Kendaraan", "No STNK"};
        DefaultTableModel dtm = new DefaultTableModel(null, kolom);
        try {
            String sql = "select * from antrian";
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            while (rs.next()) {
                String nama = rs.getString(1);
                String no_polisi = rs.getString(2);
                String no_stnk = rs.getString(3);
                String data[] = {nama, no_polisi, no_stnk};
                dtm.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        antriTabel.setModel(dtm);
    }

